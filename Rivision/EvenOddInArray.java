public class EvenOddInArray {
    public static void main(String[] args) {
        int []arr={10,5,6,7,91,100,3,54,75,65};
        System.out.println("the even number are::");
        for(int i=0;i<arr.length;i++){
            if(arr[i]%2==0){
                System.out.print(arr[i]+" ");
            }
    }
    }
}
